export function getMatchingRagas(selectedSwaras: SwaraNote[]): Array<{ raga: Raga; matchPercent: number; allSelected: boolean }> {
  if (selectedSwaras.length === 0) return [];

  return RAGAS.map((raga) => {
    const ragaSwaras = new Set([...raga.arohana, ...raga.avarohana]);
    const matchCount = selectedSwaras.filter((s) => ragaSwaras.has(s)).length;
    const matchPercent = Math.round((matchCount / selectedSwaras.length) * 100);
    const allSelected = matchCount === selectedSwaras.length;
    return { raga, matchPercent, allSelected };
  })
    .filter((r) => r.matchPercent > 0)
    .sort((a, b) => {
      if (b.allSelected !== a.allSelected) return a.allSelected ? -1 : 1;
      return b.matchPercent - a.matchPercent;
    });
}
